// Primitive data types

fn main(){
    // 1. Integers
    let x:i32 = -10;
    let y:u32 = 20;
    println!("x = {}", x);
    println!("y = {}", y);
    
    
    // 2. Floating point numbers
    let f1: f32 = 1.0;
    let f2: f64 = 2.0;
    println!("f1 = {} {}", f1, f2);
    println!("f2 = {}", f2);

    // 3. Boolean
    let b1: bool = true;
    let b2: bool = false;
    println!("b1 = {}", b1);
    println!("b2 = {}", b2);

    // 4. Characters
    let c1: char = 'a';
    let c2: char = 'b';
    println!("c1 = {}", c1);
    println!("c2 = {}", c2);
}